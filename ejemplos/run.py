# Crear dos objetos de la clase 01

class Bicicleta:
    def __init__(self, marca, modelo, velocidad):
        self.marca = marca
        self.modelo = modelo
        self.velocidad = velocidad

# objeto 01

# crear

bicicleta_1 = Bicicleta("Trek", "X-Caliber 9", 25)

# Presentar objeto; usar el método __st__

print("Mi primera bicicleta es de marca: ", bicicleta_1.marca)

# objeto 02

# crear ingresando valores por teclado

marca = input("Ingresa la marca de la segunda bicicleta: ")
modelo = input("Ingresa el modelo de la segunda bicicleta: ")
velocidad = int(input("Ingresa la velocidad de la segunda bicicleta: "))

bicicleta_2 = Bicicleta(marca, modelo, velocidad)

# Presentar objeto; usar el método __st__

print("Mi segunda bicicleta es de marca: ", bicicleta_2.marca)




